// ===== HITUNG GARIS HIDUP =====
function hitungGarisHidup(tgl, bln, thn) {
  var str = String(tgl) + String(bln) + String(thn);
  var digits = str.split('').map(Number);
  var sum = digits.reduce(function(a, b) { return a + b; }, 0);
  
  while (sum > 9) {
    var s2 = String(sum).split('').map(Number);
    sum = s2.reduce(function(a, b) { return a + b; }, 0);
  }
  return sum;
}

// ===== POPULATE DROPDOWNS =====
$(document).ready(function() {
  // Tanggal 1-31
  for (var d = 1; d <= 31; d++) {
    $('#sel-tgl').append('<option value="' + d + '">' + d + '</option>');
  }
  
  // Bulan
  var bulanNames = ['Januari','Februari','Maret','April','Mei','Juni',
                    'Juli','Agustus','September','Oktober','November','Desember'];
  for (var m = 1; m <= 12; m++) {
    $('#sel-bln').append('<option value="' + m + '">' + bulanNames[m-1] + '</option>');
  }
  
  // Tahun 1950-2026
  for (var y = 1950; y <= 2026; y++) {
    $('#sel-thn').append('<option value="' + y + '">' + y + '</option>');
  }
  
  // Set default: 1 Januari 1950
  $('#sel-tgl').val(1);
  $('#sel-bln').val(1);
  $('#sel-thn').val(1950);
  
  // ===== KIRIM BUTTON =====
  $('#btn-kirim').on('click', function() {
    var tgl = parseInt($('#sel-tgl').val());
    var bln = parseInt($('#sel-bln').val());
    var thn = parseInt($('#sel-thn').val());
    
    // Intermediate calculations
    var str = String(tgl) + String(bln) + String(thn);
    var digits = str.split('').map(Number);
    var hasil1 = digits.reduce(function(a, b) { return a + b; }, 0);
    
    var d2 = String(hasil1).split('').map(Number);
    var hasil2 = d2.reduce(function(a, b) { return a + b; }, 0);
    
    var hasilAkhir = hasil2;
    while (hasilAkhir > 9) {
      var d3 = String(hasilAkhir).split('').map(Number);
      hasilAkhir = d3.reduce(function(a, b) { return a + b; }, 0);
    }
    
    // Populate output fields
    $('#out-tgl').val(tgl);
    $('#out-bln').val(bln);
    $('#out-thn').val(thn);
    $('#out-hasil1').val(hasil1);
    $('#out-hasil2').val(hasil2 > 9 ? hasil2 : hasil2);
    $('#out-hasilakhir').val(hasilAkhir);
    
    // Show result description
    var data = garisHidupData[hasilAkhir];
    if (data) {
      $('#result-title').text(data.title);
      var html = '';
      data.paragraphs.forEach(function(p) {
        html += '<p>' + p + '</p>';
      });
      $('#result-description').html(html);
      $('#result-section').slideDown(300);
    }
  });
  
  // ===== RESET BUTTON =====
  $('#btn-reset').on('click', function() {
    $('#sel-tgl').val(1);
    $('#sel-bln').val(1);
    $('#sel-thn').val(1950);
    $('#out-tgl, #out-bln, #out-thn, #out-hasil1, #out-hasil2, #out-hasilakhir').val('');
    $('#result-section').slideUp(200);
  });
  
  // ===== BUILD LOGO PATTERN =====
  var logoSrc = document.getElementById('logo-src').src;
  var grid = document.getElementById('logo-pattern-grid');
  var count = 200;
  for (var i = 0; i < count; i++) {
    var img = document.createElement('img');
    img.src = logoSrc;
    img.alt = '';
    grid.appendChild(img);
  }
});